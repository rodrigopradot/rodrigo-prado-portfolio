/* ─── TYPEWRITER ─────────────────────────────────────────── */
const typewriterPhrases = {
  en: ['CTO & Technology Leader', 'Cloud Architect', 'Digital Transformer', 'Product Strategist'],
  es: ['CTO & Líder Tecnológico', 'Arquitecto Cloud', 'Transformador Digital', 'Estratega de Producto'],
};

let twIndex = 0;
let twCharIndex = 0;
let twDeleting = false;
let twTimeout = null;
let currentLang = 'en';

function typewriterStep() {
  const phrases = typewriterPhrases[currentLang];
  const el = document.getElementById('typewriter');
  if (!el) return;

  const current = phrases[twIndex % phrases.length];

  if (!twDeleting) {
    twCharIndex++;
    el.textContent = current.slice(0, twCharIndex);
    if (twCharIndex === current.length) {
      twDeleting = true;
      twTimeout = setTimeout(typewriterStep, 2000);
      return;
    }
    twTimeout = setTimeout(typewriterStep, 60);
  } else {
    twCharIndex--;
    el.textContent = current.slice(0, twCharIndex);
    if (twCharIndex === 0) {
      twDeleting = false;
      twIndex++;
      twTimeout = setTimeout(typewriterStep, 300);
      return;
    }
    twTimeout = setTimeout(typewriterStep, 35);
  }
}

/* ─── LANGUAGE TOGGLE ────────────────────────────────────── */
function applyLang(lang) {
  currentLang = lang;
  document.documentElement.setAttribute('data-lang', lang);
  document.documentElement.lang = lang;

  const btn = document.getElementById('langToggle');
  btn.textContent = lang === 'en' ? 'ES' : 'EN';
  btn.setAttribute('aria-label', lang === 'en' ? 'Cambiar a Español' : 'Switch to English');

  const dict = content[lang];
  document.querySelectorAll('[data-key]').forEach(el => {
    const key = el.getAttribute('data-key');
    if (dict[key] !== undefined) {
      el.innerHTML = dict[key];
    }
  });

  // Reset typewriter on lang change
  clearTimeout(twTimeout);
  const tw = document.getElementById('typewriter');
  if (tw) tw.textContent = '';
  twIndex = 0;
  twCharIndex = 0;
  twDeleting = false;
  typewriterStep();
}

/* ─── SCROLL ANIMATIONS ──────────────────────────────────── */
function initScrollObserver() {
  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add('visible');
          observer.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.12, rootMargin: '0px 0px -40px 0px' }
  );
  document.querySelectorAll('.fade-in').forEach(el => observer.observe(el));
}

/* ─── NAV SCROLL EFFECT ──────────────────────────────────── */
function initNavScroll() {
  const nav = document.getElementById('nav');
  const onScroll = () => nav.classList.toggle('scrolled', window.scrollY > 20);
  window.addEventListener('scroll', onScroll, { passive: true });
  onScroll();
}

/* ─── MOBILE MENU ────────────────────────────────────────── */
function initMobileMenu() {
  const burger = document.getElementById('navBurger');
  const links = document.getElementById('navLinks');

  burger.addEventListener('click', () => {
    const open = links.classList.toggle('open');
    burger.classList.toggle('open', open);
    burger.setAttribute('aria-expanded', open);
  });

  // Close on link click
  links.querySelectorAll('a').forEach(a => {
    a.addEventListener('click', () => {
      links.classList.remove('open');
      burger.classList.remove('open');
      burger.setAttribute('aria-expanded', false);
    });
  });
}

/* ─── FOOTER YEAR ────────────────────────────────────────── */
function setYear() {
  const el = document.getElementById('year');
  if (el) el.textContent = new Date().getFullYear();
}

/* ─── SMOOTH SCROLL OFFSET ───────────────────────────────── */
function initSmoothScroll() {
  document.querySelectorAll('a[href^="#"]').forEach(a => {
    a.addEventListener('click', e => {
      const target = document.querySelector(a.getAttribute('href'));
      if (!target) return;
      e.preventDefault();
      const offset = document.getElementById('nav').offsetHeight;
      const top = target.getBoundingClientRect().top + window.scrollY - offset;
      window.scrollTo({ top, behavior: 'smooth' });
    });
  });
}

/* ─── INIT ───────────────────────────────────────────────── */
document.addEventListener('DOMContentLoaded', () => {
  setYear();
  applyLang('en');
  initScrollObserver();
  initNavScroll();
  initMobileMenu();
  initSmoothScroll();

  document.getElementById('langToggle').addEventListener('click', () => {
    applyLang(currentLang === 'en' ? 'es' : 'en');
  });
});
